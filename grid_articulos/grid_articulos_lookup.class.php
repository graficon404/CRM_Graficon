   function lookup_activo(&$activo) 
   {
      $conteudo = "" ; 
      if ($activo == "1")
      { 
          $conteudo = "Si";
      } 
      if ($activo == "0")
      { 
          $conteudo = "No";
      } 
      if (empty($conteudo))  
      { 
          $conteudo = "1"; 
      } 
      if (!empty($conteudo)) 
      { 
          $activo = $conteudo; 
      } 
   }  
